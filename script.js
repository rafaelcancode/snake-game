    const squares = [];
    const gameBorderIndices = [];
    
    let snakeCurrentIndex;
    let direction = "ArrowRight"; // Initial direction
    let moveInterval;
  
    function createBoard() {
      for (let i = 0; i < layout.length; i++) {
        const square = document.createElement("div");
        square.id = i;
        gameContainer.appendChild(square);
        squares.push(square);
  
        if (layout[i] === 0) {
          squares[i].classList.add("game-border");
          gameBorderIndices.push(i);
        } else {
          square.classList.add("empty-space");
        }
      }
    }
  
    function startGame() {
      snakeCurrentIndex = validIndices[Math.floor(Math.random() * validIndices.length)];
      squares[snakeCurrentIndex].classList.add("snake");
      createFood();
      moveInterval = setInterval(moveSnakeAutomatically, 100); // Move every 100ms
    }
  
    function moveSnakeAutomatically() {
      // Remove snake class from previous positions
      squares.forEach(square => square.classList.remove("snake"));
  
      // Update snakeCurrentIndex based on direction
      switch (direction) {
        case "ArrowLeft":
          if (snakeCurrentIndex !== 0 && !squares[snakeCurrentIndex - 1].classList.contains("game-border")) {
            snakeCurrentIndex -= 1;
          }
          break;
        case "ArrowRight":
          if (snakeCurrentIndex !== 0 && !squares[snakeCurrentIndex + 1].classList.contains("game-border")) {
            snakeCurrentIndex += 1;
          }
          break;
        case "ArrowUp":
          if (snakeCurrentIndex - 28 >= 0 && !squares[snakeCurrentIndex - 28].classList.contains("game-border")) {
            snakeCurrentIndex -= 28;
          }
          break;
        case "ArrowDown":
          if (snakeCurrentIndex + 28 < squares.length && !squares[snakeCurrentIndex + 28].classList.contains("game-border")) {
            snakeCurrentIndex += 28;
          }
          break;
      }
  
      
  
      // Add snake class to the new position
      squares[snakeCurrentIndex].classList.add("snake");
  
      // Check for food
      foodEaten();
    }
    
  
    function handleKeyPress(e) {
      if (["ArrowLeft", "ArrowRight", "ArrowUp", "ArrowDown"].includes(e.key)) {
        direction = e.key; // Update direction
      }
    }
  
    function createFood() {
      let foodCurrentIndex;
      do {
        foodCurrentIndex = validIndices[Math.floor(Math.random() * validIndices.length)];
      } while (squares[foodCurrentIndex].classList.contains("snake"));
      
      squares[foodCurrentIndex].classList.add("snake-food");
      squares[foodCurrentIndex].classList.remove("empty-space");
    }
  
    function foodEaten() {
      if (squares[snakeCurrentIndex].classList.contains("snake-food")) {
        squares[snakeCurrentIndex].classList.remove("snake-food");
        scoreCount++; // Increase score
        scoreDisplay.textContent = `score: ${scoreCount}`;
        createFood();
      }
    }
  
    // Create the board
    createBoard();
    
    // Valid indices for the food
    const validIndices = squares.map((_, index) => index).filter(index => !gameBorderIndices.includes(index));
  
    // Add event listener for key presses
    document.addEventListener("keydown", handleKeyPress);
  
    // Start the game
    startGame();
  
